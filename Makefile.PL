		    DIST_DEFAULT => 'all tardist',
  },
  ($] ge '5.005') ? (
    'AUTHOR'   => 'Bioperl Team (bioperl-l@bioperl.org)',
    'ABSTRACT' => 'Bioperl DB',
  ) : (),
  realclean   => { FILES => join(' ', @BACKUP_FILES) },
  DIR         => [  ], # directories with other Makefile.PL's in them
  PREREQ_PM   => \%prereq,
