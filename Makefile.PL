@BACKUP_FILES = ();

# generate hash for prerequisites
# for use in WriteMakefile
my %prereq;
while (my ($key, $value) = each %packages) {
  my ($version,$desc,$expl,$module) = split( /\// , $value);                                                                          
  $prereq{$key} = $version;
}

WriteMakefile(
  NAME         => $NAME,
  DISTNAME     => $DISTNAME,
  VERSION      => $VERSION,
  dist         => { COMPRESS     => 'gzip -9f',
